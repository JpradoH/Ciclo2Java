#la funcion .replace permite remplazar valores o caracter en la funcion o cadena.
cadena = 'un uno, un dos, un tres'

print(cadena.replace('un', 'dos')) #remplaza todos los 'un' por 'dos' dentro de la cadena
print(cadena.replace('un', 'dos', 1)) #remplaza una sola vez los 'un' por 'dos' dentro de la cadena
