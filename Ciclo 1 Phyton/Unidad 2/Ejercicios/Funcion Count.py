#la funcion .count permite determinar cuantas veces se repite un caracter en la funcion o cadena.
cadena = 'un uno, un dos, un tres'

print(cadena.count('un')) #cuenta la cantidad de 'un' que hay en la varible cadena
print(cadena.count('un',10)) #establece la candidad desde una posicion inicial dada
print(cadena.count('un', 0, 10)) #establece la candidad desde una posicion inicial, hasta una final.
